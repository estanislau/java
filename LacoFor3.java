
package lacofor3;

/**
 *
 * Também é possível incrementar ou decrementar 
 * em quantidades maiores que 1, como no exemplo a seguir, 
 * que mostra todos os números pares entre 0 e 50.
 * 
 */

public class LacoFor3 {

    public static void main(String[] args) {
        
        for(int i = 0; i <= 50; i += 2){
            System.out.println(i);
        }
    }
    
}
